def soma2(v1:float, v2:float):
 '''
A função soma2, realiza a soma entre dois valores
ARGS:

v1(float): passe o número inteiro ou decimal
v2(float): passe o número inteiro ou decimal

RETURNS:
    float: retorna o número do resultado da soma entre os dois valores

'''
 return v1 + v2

def resto (v1:int, v2:int ):
    '''
    A função resto, realiza a divisão entre dois valores inteiros
    
    ARGS:
    v1(int): passe o número inteiro que será dividido
    v2(int): passe o número inteiro que será o divisor
    
    RETURNS:
        int: retorna o resto da divisão entre os dois números
    '''

    return v1 % v2

